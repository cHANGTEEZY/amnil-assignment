const string = "Hello World";

//using indices to access string
console.log(string[null]); //undefined
console.log(string[10]); //returns string at index 10

//using charAt method to access string
console.log(string.charAt()); //returns first string
console.log(string.charAt(null)); //returns first string
console.log(string.charAt(2)); //returns string at index 2
